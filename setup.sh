# Create new Vue project
npm create vue@latest lang-portal-frontend

cd lang-portal-frontend

# Install dependencies
npm install vue-router@4 pinia@2 axios@1.6 
npm install @headlessui/vue @heroicons/vue # For UI components
npm install @tailwindcss/forms @tailwindcss/typography # For styling
npm install chart.js vue-chartjs # For charts 