# Create new Vue project with TypeScript
npm create vue@latest lang-portal-frontend -- --typescript --router --pinia --eslint --prettier

cd lang-portal-frontend

# Install additional dependencies
npm install axios@1.6 
npm install @headlessui/vue @heroicons/vue 
npm install @tailwindcss/forms @tailwindcss/typography 
npm install chart.js vue-chartjs
npm install vue-toastification@2.0.0-rc.5 # For notifications 